package facisa.bd2.PetShop.Model;

public class VendaAnimal {
	
	private int NotaFiscal;
	private int RegAnimal;
	private int MatFunc;
	private int Dia;
	private int Mes;
	private int Ano;
	private double ComissaoA;
	private double Desconto;
	private double ValorFinal;
	
	
	public int getNotaFiscal() {
		return NotaFiscal;
	}
	public void setNotaFiscal(int notaFiscal) {
		NotaFiscal = notaFiscal;
	}
	public int getRegAnimal() {
		return RegAnimal;
	}
	public void setRegAnimal(int regAnimal) {
		RegAnimal = regAnimal;
	}
	public int getMatFunc() {
		return MatFunc;
	}
	public void setMatFunc(int matFunc) {
		MatFunc = matFunc;
	}
	public int getDia() {
		return Dia;
	}
	public void setDia(int dia) {
		Dia = dia;
	}
	public int getMes() {
		return Mes;
	}
	public void setMes(int mes) {
		Mes = mes;
	}
	public int getAno() {
		return Ano;
	}
	public void setAno(int ano) {
		Ano = ano;
	}
	public double getComissaoA() {
		return ComissaoA;
	}
	public void setComissaoA(double comissaoA) {
		ComissaoA = comissaoA;
	}
	public double getDesconto() {
		return Desconto;
	}
	public void setDesconto(double desconto) {
		Desconto = desconto;
	}
	public double getValorFinal() {
		return ValorFinal;
	}
	public void setValorFinal(double valorFinal) {
		ValorFinal = valorFinal;
	}
	
	

}
