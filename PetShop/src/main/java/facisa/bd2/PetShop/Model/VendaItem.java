package facisa.bd2.PetShop.Model;

public class VendaItem {
	
	private int NtFiscal;
	private int ItemCod;
	private int FuncMat;
	private int Dia;
	private int Mes;
	private int Ano;
	private double ComissaoI;
	private double Desconto;
	private double ValorF;
	
	
	public int getNtFiscal() {
		return NtFiscal;
	}
	public void setNtFiscal(int ntFiscal) {
		NtFiscal = ntFiscal;
	}
	public int getItemCod() {
		return ItemCod;
	}
	public void setItemCod(int itemCod) {
		ItemCod = itemCod;
	}
	public int getFuncMat() {
		return FuncMat;
	}
	public void setFuncMat(int funcMat) {
		FuncMat = funcMat;
	}
	public int getDia() {
		return Dia;
	}
	public void setDia(int dia) {
		Dia = dia;
	}
	public int getMes() {
		return Mes;
	}
	public void setMes(int mes) {
		Mes = mes;
	}
	public int getAno() {
		return Ano;
	}
	public void setAno(int ano) {
		Ano = ano;
	}
	public double getComissaoI() {
		return ComissaoI;
	}
	public void setComissaoI(double comissaoI) {
		ComissaoI = comissaoI;
	}
	public double getDesconto() {
		return Desconto;
	}
	public void setDesconto(double desconto) {
		Desconto = desconto;
	}
	public double getValorF() {
		return ValorF;
	}
	public void setValorF(double valorF) {
		ValorF = valorF;
	}
	
	

}
